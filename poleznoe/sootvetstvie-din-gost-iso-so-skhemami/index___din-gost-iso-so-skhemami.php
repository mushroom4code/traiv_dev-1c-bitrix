<p>Соответствие DIN-ГОСТ-ISO – это универсальная таблица соответствий стандарта DIN, ГОСТу и ISO. Таблица «Соответствие DIN-ГОСТ-ISO со схемами», была разработана для того, чтобы можно было быстрее подобрать аналог. В таблице почти каждый крепежный элемент имеет аналог, что позволяет сделать крепеж более доступным для приобретения в любой стране, так как зачастую крепежные элементы по DIN, ISO и ГОСТ не имеют значительных отличий и имеют такие же механические свойства, что и аналог.</p>

<p>
<img src="/upload/content/articles/gost.png" alt="гост" style="float:left; height:167px; margin-left:10px; margin-right:10px; width:200px"></p>

<p>На сегодняшний день в мировой практике не существует единой системы стандартизации, положениям которой подчинялись бы все металлоизделия, в том числе крепеж. Каждое государство или группа стран разрабатывают собственные стандарты, строго следуя им при выпуске продукции, в том числе на экспорт. &nbsp;В России и странах СНГ главные требования ко всем типам крепежа изложены в ГОСТах, многие из которых продолжают действовать еще с советских времен. В Германии принят стандарт DIN (Немецкий Институт Стандартизации).</p>

<p>Мощное развитие промышленного производства во всем мире, появление новых технологий в различных отраслях экономики, интенсивный товарообмен между разными странами требуют унификации крепежных изделий. Для достижения этой цели свыше 70 лет назад была создана международная организация по стандартизации ISO. После объединения большинства европейских государств в единое пространство, положениями немецких стандартов DIN пользуются практически вся Европа. Для этого во всех документах изменили даты и номера каталогов, а каждый стандарт начинается с наименования DIN EN ISO.</p>

<h2>Стандарты разные, размеры… одни</h2>

<p>В России сегодня полным ходом идет работа по совершенствованию существующей системы стандартизации и ее увязки с европейскими и международными нормативными документами. Нижеследующая таблица соответствия стандартов DIN/ГОСТ/ISO является лучшим тому доказательством. Пользуясь предложенными данными, можно легко найти замену износившейся или сломанной крепежной детали, изготовленной по DIN, среди предложений по российскому ГОСТу.</p>


<img src="/images/articles/DINENISO.jpg" style="width:100%;">

<h3>Российский государственный стандарт</h3>

<p>Наличие в маркировке любого крепежного изделия обозначения ГОСТ служит эквивалентом гарантии качества данного продукта. Это значит, что все параметры болта, гайки или шайбы точно отвечают заданным значениям. ГОСТовский крепеж надежно выполняет возложенные на него задачи по фиксации различных конструкций, стойко выдерживает предусмотренные нормативом нагрузки и воздействия внешней среды.</p>

<p>Практически все российские предприятия принимают заказы на изготовление крепежных устройств по индивидуальным чертежам клиента. Тем более, что производственное оснащение, квалифицированный персонал и современные лаборатории на каждом заводе позволяют справляться с поставленными задачами со 100% гарантией качества и соответствия требованиям ГОСТ.</p>

<h3>DIN (Deutsches Institut fur Normung)</h3>

<p>Отечественные предприятия производят небольшой процент метизов от общего потребления. По этой причине на российском рынке чаще всего встречается крепеж, изготовленный по стандарту DIN, причем на заводах Китая. Немецкий нормативный документ также излагает требования по геометрическим параметрам, конфигурации, прочности и другим величинам. Однако общеизвестно, что в ГОСТах нормы и допуски гораздо строже, нежели в стандартах DIN.</p>

<h3>ISO (International Organization for Standardization)</h3>

<p>Стандарты ISO распространяются не только на качественные и технические характеристики конкретного крепежа, но также на каждый этап производственного процесса и организационную структуру предприятия. Эффективность данной системы уже оценили практически все крупные производители крепежных устройств. Разработкой и внедрением стандартов ISO активно занимаются заинтересованные предприятия и организации в Европе, России и даже Азии.</p>

<h4>В стандартах на каждый вид крепежного изделия заложены требования, которым должны отвечать все его технические характеристики, или хотя бы основные из них:</h4>

<ul>
	<li>материал изготовления, его основные качества;</li>
	<li>конфигурация оголовка (плоская, шестигранник, полусфера, потайная, полускрытая и так далее);</li>
	<li>тип резьбовой нарезки(обычная, самонарезающая, метрическая, с широким шагом или мелким, левая или правая)</li>
	<li>диаметр рабочего стержня или отверстия в гайке/шайбе;</li>
	<li>характеристика резьбы и ее длина (полная, частичная либо другая);</li>
	<li>прочностные показатели и рекомендуемые нагрузки, завинчивающие усилия);</li>
	<li>тип антикоррозийного покрытия;</li>
	<li>аналогичные изделия для замены и прочие свойства. &nbsp;&nbsp;</li>
</ul>

<img alt="DIN ГОСТ" src="/images/tech/din_gost_perevod.jpg" vspace="5" hspace="5" border="0" align="left">


<p>Принципиальная разница между немецким DIN и российским ГОСТ состоит в применяемой системе геометрических размеров. В первом случае используются дюймы, во втором – миллиметры. Разительно отличается маркировка крепежных изделий. Отечественные стандарты более подробно указывают химический состав исходного сплава.</p>

<p>Сложно обозначить сферу экономики, которая не испытывала бы потребность в крепеже. Эти изделия нужны как при создании новых конструкций и сооружений, так и при производстве ремонтных работ. Больше всего вопросов возникает при ремонте импортного оборудования, машин, механизмов. Касательно строительства, то проблема поиска нужных крепежных изделий появляется, когда в продаже нет элементов из проектной спецификации.</p>

<p>В связи с этим возникает необходимость приобретения или оригинальных запчастей из-за рубежа, что не всегда возможно по разным причинам, или использовать российские аналоги.</p>

<p><em>Важно! Составление переводников значений для массового пользования является сложным и долгим по времени делом. Особую трудность представляет процесс перевода дюймовых значений геометрических параметров в метрическую систему.&nbsp; </em></p>

