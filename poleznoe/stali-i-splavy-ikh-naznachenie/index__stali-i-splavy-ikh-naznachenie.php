<p><strong>Сталь</strong> - сплав железа с углеродом (до 2 % С). По химическому составу сталь разделяют на углеродистую и легированную, а по качеству - на сталь обыкновенного качества, качественную, повышенного качества и высококачественную.</p>

<p>Сталь углеродистую обыкновенного качества подразделяют на три группы:</p>

<ul>
	<li>Поставляемую по механическим свойствам и применяемую в основном тогда, когда изделия из нее подвергают горячей обработке (сварка, ковка и др.), которая может изменить регламентируемые механические свойства (Ст0, Ст1 и др.);</li>
	<li>Поставляемую по химическому составу и применяемую для деталей, подвергаемых такой обработке, при которой механические свойства меняются, а уровень их кроме условий обработки определяется химическим составом (БСт0, БСт1 и др.);</li>
	<li>Поставляемую по механическим свойствам и химическому составу для деталей, подвергаемых сварке (ВСт1, ВСт2 и др.).</li>
</ul>

<p>Углеродистую сталь обыкновенного качества изготовляют следующих марок: Ст0, Ст1кп, Ст1пс, Ст1сп, Ст2кп, Ст2пс, Ст2сп, СтЗкп, СтЗпс, СтЗсп, СтЗГпс, СтЗГсп, Ст4кп, Ст4пс, Ст4сп, Ст5пс, Ст5сп, Ст5Гпс, Стбпс, Стбсп.<br />
Буквы Ст обозначают &quot;Сталь&quot;, цифры - условный номер марки в зависимости от химического состава, буквы &quot;кп&quot;, &quot;пс&quot;, &quot;сп&quot; - степень раскисления (&quot;кп&quot; - кипящая, &quot;пс&quot; - полуспокойная, &quot;сп&quot; - спокойная).</p>

<p>Сталь углеродистая качественная конструкционная по видам обработки при поставке делится на горячекатаную и кованую, калиброванную, круглую со специальной отделкой поверхности - серебрянку. По требованиям к испытанию механических свойств сталь подразделяют на пять категорий.</p>

