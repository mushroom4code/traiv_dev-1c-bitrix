<p>Легированную сталь по степени легирования разделяют на низколегированную (легирующих элементов до 2,5 %), среднелегированную (от 2,5 до 10 %), высоколегированную (от 10 до 50 %). В зависимости от основных легирующих элементов различают сталь 14 групп. К высоколегированным относят:</p>

<ul>
	<li>Коррозионностойкие (нержавеющие) стали и сплавы, обладающие стойкостью против электрохимической и химической коррозии; межкристаллитной коррозии, коррозии под напряжением и др.;</li>
	<li>Жаростойкие (окалиностойкие) стали и сплавы, обладающие стойкостью против химического разрушения в газовых средах при температуре выше 50 &deg;С, работающие в ненагруженном и слабонагруженном состоянии;</li>
	<li>Жаропрочные стали и сплавы, работающие в нагруженном состоянии при высоких температурах в течение определенного времени и обладающие при этом достаточной жаростойкостью.</li>
</ul>

