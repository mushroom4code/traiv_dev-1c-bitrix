		 — за отсутствие продаж винтов в месяце списывается 500 единиц с виртуального счета.<br>
		 — за продажи в каждом последующем месяце с динамикой +30% к проданному количеству в предыдущем месяце валюта начисляется по прогрессивной шкале, например:
