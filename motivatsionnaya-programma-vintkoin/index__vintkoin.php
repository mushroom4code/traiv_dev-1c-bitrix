	<p>
		 — за продажу установочных винтов начисляется виртуальная валюта - винтокоин, из расчета 1% от суммы выручки без ндс,&nbsp;&nbsp;т.о.&nbsp;с выручки в 1000 руб без НДС, начисляется 10 винтокоинов, при это цена продажи не должна быть ниже стоп цены, до момента автоматического запрета продажи ниже стоп цены, контроль за блоком продукта;<br>
