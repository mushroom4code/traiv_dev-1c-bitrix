<p>Traiv-Komplekt Group of Companies ist seit &uuml;ber 15 Jahren der Hauptlieferant von Befestigungs- und Montagematerialen, sowie L&ouml;sungen f&uuml;r Industrieunternehmen und Hersteller in Russland und Kasachstan, Wei&szlig;russlands und anderer GUS-Staaten.</p>

<p>Unser Kerngesch&auml;ft ist die Herstellung und der Vertrieb von industriellen Standard- und Nicht-Standard- Befestigungs- und Montagematerialen jeder Festigkeitsklasse, aus jedem Material und mit jeder Beschichtung, die f&uuml;r die spezifischen Bed&uuml;rfnisse der Hersteller aus Deutschland optimal sind.</p>


